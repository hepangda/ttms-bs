package com.hepangda.ttms.model;
import java.util.*;
public class Studio {
    int ID;
    String name;
    int row;
    int column;
    int type;


    public void setID(int ID) {
        this.ID = ID;
    }

    public void setName(String name) {
        this.name = name;
    }
    public void setType(int type) {
        this.type = type;
    }
    public void setRow(int row) {
        this.row = row;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
